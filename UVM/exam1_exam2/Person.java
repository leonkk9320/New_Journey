public class Person
{
   //instance variable
   private String name;
   private int ages;
   
   //constructor
   public Person(String n, int a)
   {
      name = n;
      age = a;
   }
   public String getName()
   {
      return name;
   }
   public getAge()
   {
      return age;
   }
   public setName(String n)
   {
      name = n;
   }
   public setAge(int a)
   {
      age = a;
   }
}