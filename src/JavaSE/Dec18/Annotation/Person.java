package JavaSE.Dec18.Annotation;
@Demo03Annotation(1)
public class Person {
    @Demo03Annotation(value = 1)
    private int age;
    private String name;

    public Person(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public Person() {
    }

    public int getAge() {
        return age;
    }
    @Demo03Annotation(value = 1, name = "leonkk", age = 23)
    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Person{" +
                "age=" + age +
                ", name='" + name + '\'' +
                '}';
    }
}
